/*
変数の型を使用したもの
double型、char型、byte型、boolean型
作成日　11/08(水)
*/
public class TypeExample{
	public static void main(String[] args){
		double d;
		d = 9.9;
		char c;
		c = 'あ';
		byte b;
		b = 81;
		boolean bool;
		bool = true;
		System.out.println("dの値は" + d);
		System.out.println("cの値は" + c);
		System.out.println("bの値は" + b);
		System.out.println("boolの値は" + bool);
	}
}
		